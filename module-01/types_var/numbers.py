# O python oferece diversos tipos de números, são eles:

# Inteiros
int_number = 4
print("Inteiro = ", int_number)

# Real com ponto flutuante
real_number = 3.14
print("Real com ponto flutuante = ", real_number)

# Type() | Objetivo é retornar o tipo da variavel
print("Tipo da variavel inteiro = ", type(int_number))
print("Tipo da variavel real = ", type(real_number))

# Operações aritiméticas

# Soma +
sum = 1 + 1
print("1 + 1 = ", sum)

# Subtração -
sub = 1 - 1
print("1 - 1 = ", sub)

# Multiplicação *
multi = 2 * 2
print("2 x 2 = ", multi)

# Divisão /
div = 5 / 2
print("5 / 2 = ", div)
print("Tipo do resultado da divisão = ", type(div))

# Converter float pra int()
print("Valor em inteiro = ", int(div))
print("Tipo do resultado da divisão depois de transformar em inteiro = ", type(int(div)))

# Converter int pra float()
print("Valor em Real = ", float(sum))
print("Tipo do resultado da soma depois de transformar em real = ", type(float(div)))

# Módulo % (Resto da divisão)
module = 5 % 2
print("Módulo = ", module)

# Divisão Inteiro //
int_div = 5 // 2
print("5 // 2 = ", int_div)
print("Tipo do resultado da divisão inteiro = ", type(int_div))